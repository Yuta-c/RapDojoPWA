function extractVowels(text) {
  // カタカナ→ひらがな変換してから母音抽出
  const hira = text.replace(/[\u30A1-\u30F6]/g, c =>
    String.fromCharCode(c.charCodeAt(0) - 0x60)
  );
  return [...hira].map(c => VOWEL_MAP[c] ?? '').join('');
}

